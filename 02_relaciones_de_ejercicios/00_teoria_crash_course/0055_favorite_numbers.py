# Favorite Numbers: Use a dictionary to store people’s favorite numbers. Think of
# five names, and use them as keys in your dictionary. Think of a favorite number for each
# person, and store each as a value in your dictionary. Print each person’s name and their
# favorite number. For even more fun, poll a few friends and get some actual data for your
# program.

favorite_numbers = {
    'sarah': 6,
    'nancy': 3,
    'mark': 4,
    'chris': 8,
    'liam': 9,
}

print(f"Sarah's favorite number is: {favorite_numbers['sarah']}")
print(f"Nancy's favorite number is: {favorite_numbers['nancy']}")
print(f"Mark's favorite number is: {favorite_numbers['mark']}")
print(f"Chris' favorite number is: {favorite_numbers['chris']}")
print(f"Liam's favorite number is: {favorite_numbers['liam']}")